    private void getCBbox(){
        PreparedStatement stm;
        ConnectDatabase.getConnection();
        try {            
            String sql="select * from qltaikhoan";
            stm = ConnectDatabase.connection.prepareStatement(sql);
            ResultSet rs = stm.executeQuery();
            while(rs.next()){
                this.cb_user.addItem(rs.getString("tenDN"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(DoiMatKhau_Frame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showConfirmDialog(this, "Lỗi.");
        }
        
    }
