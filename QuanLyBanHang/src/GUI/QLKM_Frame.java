    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
        // TODO add your handling code here:
        khuyenMai s = new khuyenMai();
        if (!batLoi.Catch(txt_magg.getText()) && !batLoi.Catch(txt_tct.getText())&& !batLoi.Catch(txt_ndkm.getText())&& !batLoi.Catch(txt_tienkm.getText())) {
            if (!bus.checkPrimaryKey(txt_magg.getText())) {
                s.setMaGiamGia(txt_magg.getText());
                s.setTenChuongTrinh(txt_tct.getText());
                s.setThoiGianBatDau(new java.sql.Date(jDateChooser6.getDate().getTime()));
                s.setThoiGianKetThuc(new java.sql.Date(jDateChooser7.getDate().getTime()));
                s.setNoiDungKhuyenMai(txt_ndkm.getText());
                s.setTienKhuyenMai(Float.parseFloat(txt_tienkm.getText()));
                bus.them(s);
                JOptionPane.showMessageDialog(rootPane, "Đã thêm");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Dữ liệu nhập vào không hợp lệ, do bị trùng khóa chính");
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Bạn chưa nhập đủ dữ liệu, vui lòng nhập lại");
        }
    }//GEN-LAST:event_addBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        int index = tbkm.getSelectedRow();
        khuyenMai s = new khuyenMai();
        s.setMaGiamGia(txt_magg.getText());
        s.setTenChuongTrinh(txt_tct.getText());
        s.setThoiGianBatDau(new java.sql.Date(jDateChooser6.getDate().getTime()));
        s.setThoiGianKetThuc(new java.sql.Date(jDateChooser7.getDate().getTime()));
        s.setNoiDungKhuyenMai(txt_ndkm.getText());
        s.setTienKhuyenMai(Float.parseFloat(txt_tienkm.getText()));
                
        model.setValueAt(s.getMaGiamGia(), index, 1);
        model.setValueAt(s.getTenChuongTrinh(), index, 2);
        model.setValueAt(s.getThoiGianBatDau(), index, 3);
        model.setValueAt(s.getThoiGianKetThuc(), index, 4);
        model.setValueAt(s.getNoiDungKhuyenMai(), index, 5);
        model.setValueAt(s.getTienKhuyenMai(), index, 6);
        bus.sua(s);
        JOptionPane.showMessageDialog(rootPane, "Đã cập nhật");
    }//GEN-LAST:event_updateBtnActionPerformed

    private void printBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_printBtnActionPerformed
        // TODO add your handling code here:
        model = (DefaultTableModel) tbkm.getModel();
        int k = 0;
        int n = model.getRowCount();
        int m = model.getColumnCount();
        String a[][] = new String[n][m];
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < m; j++) {
                try {
                    a[i][j] = model.getValueAt(i, j).toString();
                } catch (Exception e) {
                    k = 1;
                }
            };

        }
        if (k != 1) {
            Help pol = new Help();
            pol.writeFileExcel("DSPHIEUNHAP", a);
            JOptionPane.showMessageDialog(rootPane, "Đã xuất ra excel");
        } else {
            JOptionPane.showMessageDialog(rootPane, "Dữ liệu rỗng");
        }
    }//GEN-LAST:event_printBtnActionPerformed

