    public void showTable() {
        Vector header = new Vector();
        header.add("Mã giảm giá");
        header.add("Tên chương trình");
        header.add("Thời gian bắt đầu");
        header.add("Thời gian kết thúc");
        header.add("Nội dung khuyến mãi");
        header.add("Tiền khuyến mãi");
        if(model.getRowCount() == 0){
            model = new DefaultTableModel(header, 0);
        }
        for (khuyenMai s : bus.dskm) {
            model.addRow(new Object[]{
                s.getMaGiamGia(), s.getTenChuongTrinh(), s.getThoiGianBatDau(), s.getThoiGianKetThuc(), s.getNoiDungKhuyenMai(), s.getTienKhuyenMai()
            });
        }
        tbkm.setModel(model);
    }

