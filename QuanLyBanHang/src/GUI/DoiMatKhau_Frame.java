    private void btn_DongYActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_DongYActionPerformed
        //DangNhap dn = new DangNhap();
        String pass_old = txt_pass_old.getText();
        String pass_new1 = txt_pass_new1.getText();
        String pass_new2 = txt_pass_new2.getText();
        String tentk = cb_user.getSelectedItem().toString();
        if(pass_old.equals("") || pass_new1.equals("") || pass_new2.equals("")){
            JOptionPane.showMessageDialog(this,"Không để trống.");
        }
        else if(!pass_new1.equals(pass_new2)){
            JOptionPane.showMessageDialog(this, "Mật khẩu mới không khớp.");            
        }
        else if(!bus.comparePassword(pass_old).equals(pass_old)){
            JOptionPane.showMessageDialog(this, "Mật khẩu không đúng.");
            JOptionPane.showMessageDialog(this, bus.comparePassword(pass_old) );
        }
        else{
            //TaiKhoan tk = new TaiKhoan();
            
            bus.DoiMatKhau(tentk,pass_new1);
            JOptionPane.showMessageDialog(this, "Đổi thành công.");
            JOptionPane.showMessageDialog(this, tentk);
        }
    }//GEN-LAST:event_btn_DongYActionPerformed

